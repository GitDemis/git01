
$('#bloqueuno').click(function () {
    if ($('#bloqueuno').val() == "+") {
        $('#tablauno').css("display", "none");
        $('#bloqueuno').val("-");
        $('#bloqueuno').css("padding", "1px 9px");
    } else {
        $('#tablauno').css("display", "");
        $('#bloqueuno').val("+");
        $('#bloqueuno').css("padding", "1px 7px");
    }
});

